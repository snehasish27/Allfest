        function submitform() {
            //var temp= document.forms["feedback"]["name"].value;
            //console.log(temp);
            var xmlhttp = window.XMLHttpRequest ? new XMLHttpRequest() : new ActiveXObject("Microsoft.XMLHTTP");
            xmlhttp.onreadystatechange = function() {
                if (xmlhttp.readyState == 4 && xmlhttp.status == 200) {
                    console.log(xmlhttp.responseText);
                    var a = xmlhttp.responseText;
                    //var latlngStr = a.split("\n");
                    if (a == 2) {
                        alert("sorry Server Error");
                    } else if (a == 1) {
                        alert("submitted successfully");
                    }
                }

            };
            xmlhttp.open("POST", "feedback.php", true);
            xmlhttp.setRequestHeader("Content-type", "application/x-www-form-urlencoded");

            var params = "name=" + encodeURI(document.forms["feedback"]["name"].value);
            params += "&city=" + encodeURI(document.forms["feedback"]["city"].value);
            params += "&phno=" + encodeURI(document.forms["feedback"]["phno"].value);
            params += "&comment=" + encodeURI(document.forms["feedback"]["comment"].value);
            xmlhttp.send(params);
            document.getElementById("name").value="";
            document.getElementById("city").value="";
            document.getElementById("phno").value="";
            document.getElementById("comment").value="";
            return false;
        }
        //Search fuctions for the event search 

        function search() {
            //alert("lll");
            var xmlhttp = window.XMLHttpRequest ? new XMLHttpRequest() : new ActiveXObject("Microsoft.XMLHTTP");
            xmlhttp.onreadystatechange = function() {
                if (xmlhttp.readyState == 4 && xmlhttp.status == 200) {                  
                    var a = xmlhttp.responseText;
                    
                    //var latlngStr = a.split("\n");
                    if (a == 2) {
                        alert("sorry Server Error");
                    } else {
                        var $li=$(".notice-success:first");
                        
                        var events=JSON.parse(a);
                        for(var i=0;i<events.length;i++) {
                            var event=events[i];
                            
                            var newEvent=$li.clone(true);
                            newEvent.find('strong:first').html(event["festname"]+"of "+ event["colname"]+" organised by "+event["orgteam"]);
                            newEvent.find('span:first').html("-OFF_Website =<b> "+event["url"]+"</b>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-- is in month of <b>"+event["festmonth"]+"</b>");
                            
                            
                            $(".searcheve:first").append(newEvent)
                    }
                        }
                }

            };
            xmlhttp.open("POST", "search.php", true);
            xmlhttp.setRequestHeader("Content-type", "application/x-www-form-urlencoded");
            var params = "cname=" + encodeURI(document.forms["sform"]["cname"].value);
            params += "&month=" + encodeURI(document.forms["sform"]["month"].value);
            params += "&city=" + encodeURI(document.forms["sform"]["city"].value);
            xmlhttp.send(params);
            document.getElementById("cname").value="";
            document.getElementById("city").value="";
            document.getElementById("month").value="NONE";
            return false;

